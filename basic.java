public class basic {
    //This is a comment
    /*This
     * is
     * a
     * comment
     */
    
    public static void main(String[] args) throws Exception {
        // \t = Tab
        // println = Print text to new line
        System.out.println("\tHello, World!"); 

        System.out.println("Hello, World!");

        // \n = new line
        // print = print text
        System.out.print("Hello, World!\n"); 

        // \" = to add quote
        System.out.println("\"Hello, World!\"");

        // \\ = to add backslash
        System.out.println("\\Hello, World!\\");
    }
}
